        <h3>GeoSciML-Portrayal</h3>
        <p>GeoSciML-Portrayal is a now included as an application schema within the <a href="http://www.geosciml.net">GeoSciML v 4.0</a> family.</p>
        <p>GeoSciML-Portrayal delivers a flat (denormalised) view of just a small core of the full GeoSciML model. It can be used to standardise  delivery of geological map data via Web Map Services (WMS) and simple features Web Feature Services (WFS SF0).</p>
